<h3>Ficção Científica</h3>
<p class="paragrafo-centralizado">
    Séries de ficção científica exploram conceitos futuristas e especulativos, como tecnologia avançada, viagens no tempo, e vida extraterrestre. 
    Elas questionam as implicações científicas e éticas dessas ideias, criando mundos imaginativos que muitas vezes refletem preocupações contemporâneas. 
    Narrativas complexas e desenvolvimento de personagens são comuns, permitindo discussões profundas sobre a condição humana.
</p>
<p>Exemplo: Fallout</p>
