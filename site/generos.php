<h3>Super-Heróis</h3>
<p class="paragrafo-centralizado">
    Séries de super-heróis seguem personagens com habilidades extraordinárias que lutam contra vilões e ameaças. 
    Elas frequentemente exploram temas de identidade, responsabilidade e moralidade, misturando ação com elementos dramáticos. 
    As histórias podem ser ambientadas em mundos fictícios ou em cidades reais, e muitas vezes incluem um universo expandido com múltiplas histórias interconectadas.
</p>
<p>Exemplo: Superman & Lois</p>
