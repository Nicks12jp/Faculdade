<h3>Fantasia</h3>
<p class="paragrafo-centralizado">
    Séries de fantasia se passam em mundos imaginários, repletos de elementos mágicos, criaturas míticas e aventuras épicas. 
    Elas costumam explorar temas de heroísmo, poder e destino, frequentemente envolvendo uma batalha entre o bem e o mal. 
    A construção de mundos é um aspecto crucial, com mitologias e histórias que enriquecem a narrativa.
</p>
<p>Exemplo: Stranger Things</p>
