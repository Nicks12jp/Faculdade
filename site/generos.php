<h3>Drama</h3>
<p class="paragrafo-centralizado">
    Séries de drama se concentram em narrativas emocionais e personagens profundos. Elas abordam conflitos pessoais, 
    relacionamentos complexos e dilemas morais, muitas vezes explorando temas como amor, perda, e redenção. O ritmo pode variar, 
    mas o desenvolvimento do caráter e a construção de uma atmosfera emocional são centrais.
</p>
<p>Exemplo: The Boys</p>
